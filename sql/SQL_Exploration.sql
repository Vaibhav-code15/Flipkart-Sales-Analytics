-- Creating a View "sales_product "

create view sales_products as
select
    s.date_,
    s.city_name,
    s.order_id,
    s.cart_id,
    s.dim_customer_key,
    cast(s.procured_quantity as float) as procured_quantity,
    cast(s.unit_selling_price as float) as unit_selling_price,
    cast(s.total_discount_amount as float) as total_discount_amount,
    cast(s.total_weighted_landing_price as float) as total_weighted_landing_price,

    p.product_id,
    p.product_name,
    p.brand_name,
    p.manufacturer_name,
    p.l0_category,
    p.l1_category,
    p.l2_category

from sales s
inner join products$ p
    on s.product_id = p.product_id

-- Revenue generated by each l0 category

select l0_category,COUNT(DISTINCT order_id) as Orders,
SUM(CAST(procured_quantity AS float)) as Units_Sold,
round(SUM(CAST(procured_quantity AS float)* CAST(unit_selling_price AS float)
- CAST(total_discount_amount AS float)),2) as Revenue
from sales_products
group by l0_category
order by Revenue DESC

-- Top 15 brands by Revenue

select top 15 brand_name as Brand_Name,COUNT(DISTINCT order_id) AS Orders,
SUM(CAST(procured_quantity as float)) AS Units_Sold,
SUM(CAST(procured_quantity as float)* CAST(unit_selling_price as float)
- CAST(total_discount_amount as float))  AS Revenue
from sales_products
where brand_name is not NULL
group by brand_name
order by Revenue DESC

-- Top 10 Manufacturers by Revenue

select top 10 manufacturer_name as Manufacturer_Name,COUNT(DISTINCT order_id) AS Orders,
SUM(CAST(procured_quantity as float)) AS Units_Sold,
SUM(CAST(procured_quantity as float)* CAST(unit_selling_price as float)
- CAST(total_discount_amount as float))  AS Revenue
from sales_products
where manufacturer_name is not NULL
group by manufacturer_name
order by Revenue DESC

-- Top 15 Products by Revenue

select top 15 product_name as Product_Name,
count(distinct order_id) as Orders,
sum(cast(procured_quantity as float)) as Units_sold,
sum((cast(procured_quantity as float)* cast(unit_selling_price as float))
- cast(total_discount_amount as float)) as Revenue
from sales_products
group by product_name
order by Revenue desc

-- Looking at Discount intensity by category

select l0_category,round(sum(total_discount_amount),2) as Total_discount,
round(sum(procured_quantity * unit_selling_price),2) as Gross_Revenue,
round(sum(total_discount_amount) * 100.0 /sum(procured_quantity * unit_selling_price),2) 
as Discount_percentage
from sales_products
group by l0_category
order by Discount_percentage desc

--  Looking at the Gross margin per category (selling price - landing price)

select l0_category,round(avg(unit_selling_price),2) as Avg_Selling_price,
round(avg(total_weighted_landing_price),2) as Avg_Landing_price,
round(avg(unit_selling_price - total_weighted_landing_price),2) as Avg_Margin_per_unit,
round(( avg( unit_selling_price - total_weighted_landing_price ) 
/nullif(avg(unit_selling_price),0)) * 100, 2) as Margin_percentage
from sales_products
group by l0_category
order by Margin_percentage desc;


