-- Estimated Profit by Each category

select l0_category,
count(distinct order_id) as Orders,
sum(procured_quantity) as Units_Sold,
round(sum( ( unit_selling_price * procured_quantity ) - total_weighted_landing_price 
- total_discount_amount),2) as Estimated_Profit
from sales_products
group by l0_category
order by estimated_profit desc;
